// Helper to get random wrong choices
// Simplified to just take random available readings from the dataset.
function getRandomReadings(correctReading: string, count: number): string[] {
    const allReadings = Array.from(new Set(KANJI_DATA.map(k => k.reading)));
    // Filter out correct reading
    const otherReadings = allReadings.filter(r => r !== correctReading);

    // Sort random
    const shuffled = otherReadings.sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
}

export interface QuizQuestion extends KanjiProblem {
    choices: string[];
}

// Helper to get 10 random questions with generated choices
export function getRandomQuestions(count: number = 10): QuizQuestion[] {
    const shuffled = [...KANJI_DATA].sort(() => 0.5 - Math.random()).slice(0, count);

    return shuffled.map(question => {
        const wrongReadings = getRandomReadings(question.reading, 3);
        const choices = [...wrongReadings, question.reading].sort(() => 0.5 - Math.random());
        return {
            ...question,
            choices
        };
    });
}
